   FROM ("public"."itens_demanda" "i"
     JOIN "public"."demandas_producao" "d" ON (("i"."demanda_id" = "d"."id")))
  WHERE (("d"."status")::"text" <> ALL ((ARRAY['Finalizado'::character varying, 'Coletado'::character varying, 'Cancelado'::character varying])::"text"[]));


